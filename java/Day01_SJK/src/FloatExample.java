
public class FloatExample {
	
	public static void main(String[] args) {
		//실수 리터럴의 기본타입은 double
		float f1 = 7.12345F;
		double d1 = 7.12345;
		
		System.out.printf("%f,%f\n",f1,d1);
		
		float f2 = 1.234567891234f;
		double d2 = 1.234567891234;
		
		System.out.println(f2);
		System.out.println(d2);
	}
}
